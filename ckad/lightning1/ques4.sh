#!/bin/bash


cat <<EOF > /root/ques4.yaml
---
apiVersion: apps/v1
kind: Deployment
metadata:
  labels:
    app: nginx-deploy
  name: nginx-deploy
  namespace: default
spec:
  replicas: 4
  selector:
    matchLabels:
      app: nginx-deploy
  strategy:
    rollingUpdate:
      maxSurge: 1
      maxUnavailable: 2
    type: RollingUpdate
  template:
    metadata:
      labels:
        app: nginx-deploy
    spec:
      containers:
      - image: nginx:1.16
        imagePullPolicy: IfNotPresent
        name: nginx

EOF


kubectl apply -f /root/ques4.yaml

kubectl set image deployment nginx-deploy nginx=nginx:1.17


kubectl rollout undo deployment nginx-deploy
